"""Generate the CPU test requirements input from the CUDA one.

The CPU backend does not maintain a dedicated ``requirements/test/cpu.in``.
Instead it is derived from ``requirements/test/cuda.in`` by applying a small,
ordered list of transforms (drop a package, pin/unpin a version, ...). This
keeps the two backends in sync while allowing a handful of CPU-specific
overrides.

To add a new special case, append a ``Transform`` to ``TRANSFORMS`` below.

Usage:
    python tools/pre_commit/generate_cpu_test.py            # rewrite cpu.in
    python tools/pre_commit/generate_cpu_test.py --check    # verify only
"""

import argparse
import sys

import regex

SOURCE_FILE = "requirements/test/cuda.in"
TARGET_FILE = "requirements/test/cpu.in"

HEADER = (
    "# This file is autogenerated from requirements/test/cuda.in by\n"
    "# tools/pre_commit/generate_cpu_test.py. Do not edit it manually.\n"
    "# Add CPU-specific overrides to TRANSFORMS in that script instead.\n"
)


def _name_pattern(package: str) -> regex.Pattern[str]:
    """Compile a matcher for ``package`` at the start of a requirement line.

    Names are matched case-insensitively with ``-`` and ``_`` treated as
    equivalent, and only up to a delimiter so e.g. ``torch`` does not match
    ``torchaudio``.
    """
    escaped = r"[-_]".join(regex.escape(part) for part in regex.split(r"[-_]", package))
    return regex.compile(rf"^{escaped}(?=[\s\[<>=!~;#]|$)", regex.IGNORECASE)


class Transform:
    """Base class: rewrite a single requirement, or drop it by returning None."""

    def __init__(self, package: str):
        self.package = package
        self._matches = _name_pattern(package)

    def applies_to(self, requirement: str) -> bool:
        return self._matches.match(requirement) is not None

    def apply(self, requirement: str) -> str | None:
        raise NotImplementedError


class DropPackage(Transform):
    """Remove the package entirely."""

    def apply(self, requirement: str) -> str | None:
        return None


class Pin(Transform):
    """Force an exact ``package==version``, preserving markers and comments."""

    def __init__(self, package: str, version: str):
        super().__init__(package)
        self.version = version

    def apply(self, requirement: str) -> str | None:
        name, _, marker, comment = _split(requirement)
        return _join(f"{name}=={self.version}", marker, comment)


class Unpin(Transform):
    """Strip the version specifier, preserving markers and comments."""

    def apply(self, requirement: str) -> str | None:
        name, _, marker, comment = _split(requirement)
        return _join(name, marker, comment)


# Ordered transforms applied to each line of cuda.in. Append new CPU-specific
# overrides here.
TRANSFORMS: list[Transform] = [
    DropPackage("mamba_ssm"),
    Pin("torch", "2.11.0"),
    Unpin("torchaudio"),
    Unpin("torchvision"),
    # https://github.com/vllm-project/vllm/pull/38800#issuecomment-4228314305
    Pin("sentence-transformers", "5.3.0"),
]


def _split(requirement: str) -> tuple[str, str, str, str]:
    """Split into (name+extras, spec, marker, comment)."""
    body, sep, comment = requirement.partition("#")
    comment = (sep + comment).strip()
    body, sep, marker = body.partition(";")
    marker = marker.strip()
    body = body.strip()
    m = regex.match(r"^([A-Za-z0-9._-]+(?:\[[^\]]*\])?)\s*(.*)$", body)
    name, spec = (m.group(1), m.group(2)) if m else (body, "")
    return name, spec, marker, comment


def _join(name: str, marker: str, comment: str) -> str:
    out = name
    if marker:
        out += f"; {marker}"
    if comment:
        out += f" {comment}"
    return out


def transform_line(line: str) -> str | None:
    stripped = line.strip()
    if not stripped or stripped.startswith("#"):
        return line.rstrip("\n")
    for transform in TRANSFORMS:
        if transform.applies_to(stripped):
            return transform.apply(stripped)
    return line.rstrip("\n")


def generate() -> str:
    with open(SOURCE_FILE) as f:
        lines = [transform_line(line) for line in f]
    body = "\n".join(line for line in lines if line is not None)
    return HEADER + body.rstrip("\n") + "\n"


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="verify only")
    args = parser.parse_args()

    content = generate()
    if args.check:
        with open(TARGET_FILE) as f:
            if f.read() != content:
                sys.stderr.write(f"{TARGET_FILE} is out of date; regenerate it.\n")
                return 1
        return 0
    with open(TARGET_FILE, "w") as f:
        f.write(content)
    return 0


if __name__ == "__main__":
    sys.exit(main())
