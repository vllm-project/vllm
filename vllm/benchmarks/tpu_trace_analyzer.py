"""
TPU Trace Analyzer for XLA profiler traces.

Analyzes TPU performance from Chrome trace format JSON files generated by
JAX/XLA profiler. Categorizes XLA operations and provides per-iteration breakdowns.

Output format matches the GPU trace analyzer (D89429150) for consistency.
"""

from __future__ import annotations

import argparse
import dataclasses
import gzip
import json
import logging
import re
import sys
from collections import defaultdict
from pathlib import Path
from typing import Any, Optional

# Configure logging to match vLLM style
logging.basicConfig(
    level=logging.INFO,
    format="INFO %(asctime)s [%(filename)s:%(lineno)d] %(message)s",
    datefmt="%m-%d %H:%M:%S",
)
logger = logging.getLogger(__name__)

# =============================================================================
# tf_op-based Classification (similar to GPU's CPU-stack classification)
# =============================================================================
# TPU traces have a `tf_op` field that provides semantic context about which
# high-level operation (MoE, Attention, etc.) triggered the XLA op.
# This is more accurate than kernel name matching alone.

# tf_op patterns for classification - ordered by priority (higher = more specific)
TF_OP_CATEGORY_PATTERNS: dict[str, list[str]] = {
    # MoE operations (highest priority - MoE GEMM should be MoE, not GEMM)
    "MoE": [
        "FusedMoE",
        "fused_moe",
        "moe_forward",
        "jit(gmm)",  # Group matrix multiply in MoE context
        "expert_offset",
        "topk_gate",
        "moe_permute",
    ],
    # Attention operations
    "Attn": [
        "_jax_attn_func",
        "ragged_paged_attention",
        "paged_attention",
        "flash_attention",
        "QKVParallelLinear",  # QKV projection is part of attention
        "self_attn",
        "RPA-HD",
    ],
    # Communication operations
    "Comm": [
        "/psum",
        "all-reduce",
        "all_reduce",
        "all-gather",
        "all_gather",
    ],
    # Normalization operations
    "Norm": [
        "RMSNorm",
        "LayerNorm",
        "rms_norm",
        "layer_norm",
    ],
    # GEMM/Linear operations (lower priority - generic linear not in Attn/MoE)
    "GEMM": [
        "RowParallelLinear",
        "ColumnParallelLinear",
        "/linear/",
        "dot_general",
        "mn,pn->mp",  # einsum pattern for matmul
    ],
    # Embedding operations
    "Embed": [
        "VocabParallel",
        "embedding",
    ],
}

# Priority order for tf_op categories (checked first to last)
TF_OP_CATEGORY_PRIORITY: list[str] = ["MoE", "Attn", "Comm", "Norm", "GEMM", "Embed"]


def classify_from_tf_op(tf_op: str) -> Optional[str]:
    """
    Classify an XLA operation based on its tf_op context.

    Args:
        tf_op: The tf_op string from the trace event args

    Returns:
        Category string if matched, None otherwise
    """
    if not tf_op:
        return None

    tf_op_lower = tf_op.lower()

    for category in TF_OP_CATEGORY_PRIORITY:
        patterns = TF_OP_CATEGORY_PATTERNS.get(category, [])
        for pattern in patterns:
            if pattern.lower() in tf_op_lower:
                return category

    return None


@dataclasses.dataclass
class TPUOpStats:
    """Statistics for a single XLA operation or group of operations."""

    name: str
    category: str
    total_time_ms: float  # Aggregate time across all devices per iteration
    avg_time_per_call_ms: float
    num_calls: float  # Can be fractional when averaged per iteration
    percentage_of_total: float
    time_per_device_ms: float = 0.0  # Per-device time (total_time_ms / num_devices)
    original_name: str = ""


def classify_xla_op_category(op_name: str) -> str:
    """
    Classify XLA operation into categories based on pattern matching.

    Categories: MoE, Attn, Norm, Comm, GEMM, Elementwise, Memory, Others

    This classification is heuristic-based, similar to the GPU trace analyzer.
    For more accurate classification using tf_op context, use classify_xla_op_hybrid().
    """
    name_lower = op_name.lower()

    # MoE operations - check first as MoE GEMM should be MoE, not GEMM
    # Note: In practice, gmm without tf_op context is ambiguous
    moe_patterns = [
        "expert_",
        "moe_",
        "topk_gate",
    ]
    for p in moe_patterns:
        if p in name_lower:
            return "MoE"

    # Attention operations
    attn_patterns = [
        "rpa-hd",  # Ring Paged Attention (vLLM TPU) - e.g., RPA-HD_64-bq_8
        "rpa_hd",
        "flash_attention",
        "flash-attention",
        "paged_attention",
        "paged-attention",
        "attention",
        "fmha",  # Flash Multi-Head Attention
    ]
    for p in attn_patterns:
        if p in name_lower:
            return "Attn"

    # Normalization operations - separate from Elementwise for better granularity
    norm_patterns = [
        "rmsnorm",
        "rms_norm",
        "rms-norm",
        "layernorm",
        "layer_norm",
        "layer-norm",
    ]
    for p in norm_patterns:
        if p in name_lower:
            return "Norm"

    # Communication operations
    comm_patterns = [
        "all-reduce",
        "all_reduce",
        "allreduce",
        "all-gather",
        "all_gather",
        "allgather",
        "reduce-scatter",
        "reduce_scatter",
        "reducescatter",
        "psum",  # TPU-specific parallel sum
        "pmean",
        "ppermute",
        "pswapaxes",
        "collective",
        "send",
        "recv",
    ]
    for p in comm_patterns:
        if p in name_lower:
            return "Comm"

    # GEMM operations (matrix multiply)
    gemm_patterns = [
        "gmm",  # Grouped matrix multiply (TPU-specific)
        "dot",
        "dot_general",
        "einsum",
        "matmul",
        "convolution fusion",  # Dense GEMM on TPU often shows as convolution fusion
    ]
    for p in gemm_patterns:
        if name_lower.startswith(p) or f"_{p}" in name_lower or f"-{p}" in name_lower:
            return "GEMM"
    # Also check hlo_category pattern
    if "convolution" in name_lower:
        return "GEMM"

    # Memory operations
    memory_patterns = [
        "copy-start",
        "copy-done",
        "copy.",
        "memcpy",
        "memset",
    ]
    for p in memory_patterns:
        if p in name_lower or name_lower.startswith("copy"):
            return "Memory"

    # Elementwise and other compute operations
    elem_patterns = [
        "fusion",
        "slice",
        "pad",
        "reshape",
        "broadcast",
        "transpose",
        "reduce",
        "scatter",
        "gather",
        "select",
        "compare",
        "clamp",
        "convert",
        "bitcast",
        "dynamic-slice",
        "dynamic_slice",
        "dynamic-update-slice",
        "add",
        "multiply",
        "subtract",
        "divide",
        "maximum",
        "minimum",
        "sort",
        "top_k",
        "top-k",
        "iota",
        "constant",
        "tuple",
        "get-tuple-element",
        "concatenate",
        "reverse",
        "reduce-window",
        "softmax",
        "custom-call",
        "custom fusion",
    ]
    for p in elem_patterns:
        if name_lower.startswith(p) or f"_{p}" in name_lower:
            return "Elementwise"

    return "Others"


def classify_xla_op_hybrid(
    op_name: str,
    tf_op: Optional[str] = None,
    hlo_category: Optional[str] = None,
) -> str:
    """
    Classify XLA operation using tf_op context (primary) with kernel name fallback.

    This provides more accurate classification by using the semantic context
    from the tf_op field, similar to the GPU trace analyzer's CPU-stack classification.

    Args:
        op_name: The XLA operation name
        tf_op: Optional tf_op string from event args (provides semantic context)
        hlo_category: Optional hlo_category from event args

    Returns:
        Category string
    """
    # Try tf_op-based classification first (more semantic)
    if tf_op:
        category = classify_from_tf_op(tf_op)
        if category:
            return category

    # Fall back to kernel name matching
    return classify_xla_op_category(op_name)


def normalize_op_name(op_name: str) -> str:
    """
    Normalize XLA operation name by removing numeric suffixes.

    Examples:
        "fusion.123" -> "fusion"
        "copy.1" -> "copy"
        "RPA-HD_64-bq_8-bkvp_128" -> "RPA-HD_64-bq_8-bkvp_128" (preserved)
    """
    # Remove trailing .N suffix (e.g., fusion.123 -> fusion)
    name = re.sub(r"\.\d+$", "", op_name)
    return name


def _check_execute_model_requests(events: list[dict[str, Any]]) -> None:
    """Check execute_model events for multi-request iterations (possible prefill in decode trace)."""
    req_counts: set[int] = set()
    tok_counts: set[int] = set()
    for event in events:
        name = str(event.get("name", ""))
        m = re.match(r"execute_model: (\d+) reqs, (\d+) toks", name)
        if m:
            req_counts.add(int(m.group(1)))
            tok_counts.add(int(m.group(2)))
    if not req_counts:
        return
    logger.info(f"execute_model request counts: {sorted(req_counts)}, token counts: {sorted(tok_counts)}")
    multi = sorted(c for c in req_counts if c > 1)
    if multi:
        logger.warning(
            f"ALERT: execute_model has >1 requests {multi} — "
            f"trace may include prefill step(s) in a decode trace"
        )


def extract_execute_model_pairs(
    events: list[dict[str, Any]],
) -> list[tuple[int, int]]:
    """Extract unique (reqs, toks) pairs from execute_model TraceAnnotation events.

    Returns:
        List of (reqs, toks) tuples, sorted, with (0, 0) filtered out.
    """
    pairs: set[tuple[int, int]] = set()
    for event in events:
        name = str(event.get("name", ""))
        m = re.match(r"execute_model: (\d+) reqs, (\d+) toks", name)
        if m:
            reqs, toks = int(m.group(1)), int(m.group(2))
            if reqs > 0 or toks > 0:
                pairs.add((reqs, toks))
    return sorted(pairs)


def check_execute_model_requests(trace_path: str) -> None:
    """Load a trace and check execute_model request/token counts."""
    logger.info(f"Checking execute_model requests in: {trace_path}")
    events, _ = parse_tpu_trace(trace_path)
    logger.info(f"Loaded {len(events)} events")
    _check_execute_model_requests(events)


def get_tpu_pids(events: list[dict[str, Any]]) -> dict[int, str]:
    """
    Extract TPU device PIDs and their names from trace metadata.

    TPU devices are identified by process_name containing "/device:TPU:"

    Returns:
        Dict mapping pid to device name (e.g., {3: "/device:TPU:0"})
    """
    tpu_pids = {}
    for event in events:
        if event.get("ph") == "M" and event.get("name") == "process_name":
            proc_name = event.get("args", {}).get("name", "")
            if "/device:TPU:" in proc_name:
                tpu_pids[event.get("pid")] = proc_name
    return tpu_pids


def get_thread_names(events: list[dict[str, Any]]) -> dict[tuple[int, int], str]:
    """
    Extract thread names from trace metadata.

    Returns:
        Dict mapping (pid, tid) to thread name
    """
    thread_names = {}
    for event in events:
        if event.get("ph") == "M" and event.get("name") == "thread_name":
            pid = event.get("pid")
            tid = event.get("tid")
            name = event.get("args", {}).get("name", "")
            thread_names[(pid, tid)] = name
    return thread_names


def parse_tpu_trace(trace_path: str) -> tuple[list[dict[str, Any]], dict[int, str]]:
    """
    Parse TPU profiler trace file (gzipped JSON in Chrome trace format).

    Args:
        trace_path: Path to trace file (.json.gz or .json)

    Returns:
        Tuple of (all_events, tpu_pids_dict)
    """
    path = Path(trace_path)
    if not path.exists():
        raise FileNotFoundError(f"Trace file not found: {trace_path}")

    # Open and parse gzipped or plain JSON
    if path.suffix == ".gz" or str(path).endswith(".json.gz"):
        with gzip.open(trace_path, "rt") as f:
            trace_data = json.load(f)
    else:
        with open(trace_path) as f:
            trace_data = json.load(f)

    events = trace_data.get("traceEvents", [])
    tpu_pids = get_tpu_pids(events)

    return events, tpu_pids


def extract_xla_ops(
    events: list[dict[str, Any]],
    tpu_pids: dict[int, str],
    xla_ops_tid: int = 3,
) -> list[dict[str, Any]]:
    """
    Extract XLA Ops events from TPU devices.

    In TPU traces:
    - TID 2: XLA Modules (high-level execution blocks)
    - TID 3: XLA Ops (individual XLA operations) <- main target
    - TID 7: TC Overlay (tensor core overlay)

    Args:
        events: All trace events
        tpu_pids: Dict of TPU device PIDs
        xla_ops_tid: Thread ID for XLA Ops (default: 3)

    Returns:
        List of XLA op events with duration > 0
    """
    xla_ops = []
    for event in events:
        if (
            event.get("ph") == "X"
            and event.get("pid") in tpu_pids
            and event.get("tid") == xla_ops_tid
            and event.get("dur", 0) > 0
        ):
            xla_ops.append(event)
    return xla_ops


def identify_iteration_boundaries(
    events: list[dict[str, Any]],
    tpu_pids: dict[int, str],
    iteration_prefix: str = "jit_step_fun",
) -> list[tuple[int, int]]:
    """
    Identify iteration boundaries from XLA Modules events.

    Looks for module executions matching the iteration_prefix in XLA Modules (TID 2).
    Uses the first TPU device to identify boundaries (they should be synchronized).

    Args:
        events: All trace events
        tpu_pids: Dict of TPU device PIDs
        iteration_prefix: Prefix for iteration boundary events

    Returns:
        List of (start_time_us, end_time_us) tuples for each iteration
    """
    iterations = []
    xla_modules_tid = 2
    iteration_names: set[str] = set()

    # Use first TPU device for iteration boundaries
    first_tpu_pid = min(tpu_pids.keys()) if tpu_pids else None
    if first_tpu_pid is None:
        return []

    for event in events:
        if (
            event.get("ph") == "X"
            and event.get("pid") == first_tpu_pid
            and event.get("tid") == xla_modules_tid
        ):
            name = event.get("name", "")
            if name.startswith(iteration_prefix):
                iteration_names.add(name)
                ts = event.get("ts", 0)
                dur = event.get("dur", 0)
                if dur > 0:
                    iterations.append((int(ts), int(ts + dur)))

    if not iterations:
        logger.warning(f"No events with prefix '{iteration_prefix}' found")
        return []

    logger.info(f"Found {len(iterations)} iterations with '{iteration_prefix}*' events")
    if iteration_names:
        logger.debug(f"Unique iteration event names: {iteration_names}")

    # Sort by start time
    iterations.sort()

    # Merge overlapping intervals
    if not iterations:
        return []

    merged = [iterations[0]]
    merge_count = 0
    for start, end in iterations[1:]:
        last_start, last_end = merged[-1]
        if start <= last_end:
            merged[-1] = (last_start, max(last_end, end))
            merge_count += 1
        else:
            merged.append((start, end))

    if merge_count > 0:
        logger.info(f"Merged {merge_count} overlapping iteration events")

    logger.info(f"Final iteration count: {len(merged)} iterations")

    return merged


def aggregate_xla_ops(
    xla_ops: list[dict[str, Any]],
    iteration_boundaries: Optional[list[tuple[int, int]]] = None,
    num_devices: int = 1,
    use_tf_op: bool = True,
) -> list[TPUOpStats]:
    """
    Aggregate XLA operation statistics.

    Args:
        xla_ops: List of XLA op events
        iteration_boundaries: Optional list of (start, end) for per-iteration averaging
        num_devices: Number of TPU devices for per-device time calculation
        use_tf_op: If True, use tf_op field for classification (more accurate)

    Returns:
        List of TPUOpStats sorted by total time descending
    """
    # Aggregate by normalized operation name
    op_aggregates: dict[str, dict[str, Any]] = defaultdict(
        lambda: {"total_time_us": 0, "count": 0, "original_name": "", "category": None}
    )

    for event in xla_ops:
        name = event.get("name", "Unknown")
        dur = event.get("dur", 0)

        # Get tf_op and hlo_category from args
        args = event.get("args", {})
        tf_op = args.get("tf_op", "") if use_tf_op else ""
        hlo_category = args.get("hlo_category", "")

        normalized = normalize_op_name(name)
        op_aggregates[normalized]["total_time_us"] += dur
        op_aggregates[normalized]["count"] += 1
        if not op_aggregates[normalized]["original_name"]:
            op_aggregates[normalized]["original_name"] = name

        # Compute category using hybrid classification
        # Store the first category we compute (they should all be the same for same op)
        if op_aggregates[normalized]["category"] is None:
            if use_tf_op and tf_op:
                op_aggregates[normalized]["category"] = classify_xla_op_hybrid(
                    name, tf_op, hlo_category
                )
            else:
                op_aggregates[normalized]["category"] = classify_xla_op_category(name)

    # Calculate total time for percentage
    total_time_us = sum(agg["total_time_us"] for agg in op_aggregates.values())

    # Determine divisor for per-iteration stats
    num_iters = len(iteration_boundaries) if iteration_boundaries else 1

    # Convert to TPUOpStats
    stats_list = []
    for normalized_name, agg in op_aggregates.items():
        category = agg["category"] or classify_xla_op_category(agg["original_name"])

        time_per_iter_ms = (agg["total_time_us"] / num_iters) / 1000.0
        calls_per_iter = agg["count"] / num_iters
        avg_per_call_ms = time_per_iter_ms / calls_per_iter if calls_per_iter > 0 else 0
        percentage = (
            (agg["total_time_us"] / total_time_us * 100) if total_time_us > 0 else 0
        )
        time_per_device_ms = time_per_iter_ms / num_devices if num_devices > 0 else 0

        stats_list.append(
            TPUOpStats(
                name=normalized_name,
                category=category,
                total_time_ms=time_per_iter_ms,
                avg_time_per_call_ms=avg_per_call_ms,
                num_calls=calls_per_iter,
                percentage_of_total=percentage,
                time_per_device_ms=time_per_device_ms,
                original_name=agg["original_name"],
            )
        )

    stats_list.sort(key=lambda x: x.total_time_ms, reverse=True)
    return stats_list


def _compute_category_breakdown(
    trace_path: str,
    iteration_prefix: str = "jit_step_fun",
    use_tf_op: bool = True,
) -> tuple[dict[str, float], float]:
    """
    Shared logic for computing the per-category time breakdown from a TPU trace.

    Parses the trace, identifies iterations, extracts and aggregates XLA ops,
    then sums per-category total_time_ms values.

    Args:
        trace_path: Path to the trace file (.json.gz or .json)
        iteration_prefix: Prefix for iteration boundary events
        use_tf_op: If True, use tf_op field for more accurate classification

    Returns:
        Tuple of (category_breakdown, avg_iteration_duration_ms, request_pairs):
        - category_breakdown: Dict mapping category name to time in ms per iteration per device
        - avg_iteration_duration_ms: Average wall-clock iteration duration in ms
        - request_pairs: List of (reqs, toks) pairs from execute_model annotations
    """
    # Parse trace
    events, tpu_pids = parse_tpu_trace(trace_path)

    # Check execute_model request counts
    _check_execute_model_requests(events)

    # Extract (reqs, toks) pairs from execute_model annotations
    request_pairs = extract_execute_model_pairs(events)

    # Identify iterations
    iterations = identify_iteration_boundaries(events, tpu_pids, iteration_prefix)

    # Extract XLA ops
    xla_ops = extract_xla_ops(events, tpu_pids)

    if not xla_ops:
        logger.warning("No XLA operations found in trace.")
        return {}, 0.0, request_pairs

    # Count devices that actually have XLA op events (not all TPU PIDs from metadata)
    num_devices = len({e["pid"] for e in xla_ops})

    # Aggregate statistics
    stats = aggregate_xla_ops(
        xla_ops,
        iterations if iterations else None,
        num_devices=num_devices,
        use_tf_op=use_tf_op,
    )

    # Compute average iteration duration from boundaries
    avg_iter_duration_ms = 0.0
    if iterations:
        durations_ms = [(end - start) / 1000.0 for start, end in iterations]
        avg_iter_duration_ms = sum(durations_ms) / len(durations_ms)

    # Build category breakdown (per-device, not total across all devices)
    category_totals: dict[str, float] = defaultdict(float)
    for s in stats:
        category_totals[s.category] += s.time_per_device_ms

    return dict(category_totals), avg_iter_duration_ms, request_pairs


def analyze_tpu_trace_breakdown(
    trace_path: str,
    iteration_prefix: str = "jit_step_fun",
    use_tf_op: bool = True,
) -> tuple[dict[str, float], float, list[tuple[int, int]]]:
    """Analyze TPU trace and return category breakdown and iteration duration.

    Args:
        trace_path: Path to the trace file (.json.gz or .json)
        iteration_prefix: Prefix for iteration boundary events
        use_tf_op: If True (default), use tf_op field for more accurate
            semantic classification

    Returns:
        Tuple of (category_breakdown, avg_iteration_duration_ms, request_pairs):
        - category_breakdown: Dict mapping category name to per-device per-iteration ms
        - avg_iteration_duration_ms: Average wall-clock iteration duration in ms
        - request_pairs: List of (reqs, toks) pairs from execute_model annotations
    """
    return _compute_category_breakdown(
        trace_path=trace_path,
        iteration_prefix=iteration_prefix,
        use_tf_op=use_tf_op,
    )


def analyze_tpu_trace(
    trace_path: str,
    iteration_prefix: str = "jit_step_fun",
    top_n: int = 20,
    max_types: int = 1,
    csv_output: bool = False,
    use_tf_op: bool = True,
) -> None:
    """
    Analyze TPU profiler trace and print performance summary.

    Output format matches GPU trace analyzer (D89429150) for consistency.

    Args:
        trace_path: Path to the trace file (.json.gz)
        iteration_prefix: Prefix for iteration boundary events
        top_n: Number of top operations to display
        max_types: Maximum number of iteration types to display (for compatibility)
        csv_output: If True, also print CSV format
        use_tf_op: If True (default), use tf_op field for more accurate classification
    """
    logger.info(f"Analyzing trace file: {trace_path}")
    logger.info(f"Iteration prefix: {iteration_prefix}")
    logger.info(f"Use tf_op classification: {use_tf_op}")

    # Parse trace
    logger.info("Loading trace file...")
    events, tpu_pids = parse_tpu_trace(trace_path)
    logger.info(f"Loaded {len(events)} events from trace file")
    logger.info(f"Found {len(tpu_pids)} TPU PIDs in trace metadata")

    # Check execute_model request counts
    _check_execute_model_requests(events)

    # Identify iterations
    logger.info("Identifying iteration boundaries...")
    iterations = identify_iteration_boundaries(events, tpu_pids, iteration_prefix)

    # Extract XLA ops
    logger.info("Filtering XLA Op events...")
    xla_ops = extract_xla_ops(events, tpu_pids)
    logger.info(f"Found {len(xla_ops)} XLA Op events")

    if not xla_ops:
        logger.warning("No XLA operations found in trace.")
        return

    # Count devices that actually have XLA op events (not all TPU PIDs from metadata)
    num_devices = len({e["pid"] for e in xla_ops})
    logger.info(f"Active TPU devices (with XLA ops): {num_devices}")

    # Aggregate statistics
    logger.info("Aggregating operation statistics across iterations...")
    stats = aggregate_xla_ops(
        xla_ops,
        iterations if iterations else None,
        num_devices=num_devices,
        use_tf_op=use_tf_op,
    )

    # Iteration duration statistics (matching GPU analyzer format)
    if iterations:
        durations_ms = [(end - start) / 1000.0 for start, end in iterations]
        avg_dur = sum(durations_ms) / len(durations_ms)
        min_dur = min(durations_ms)
        max_dur = max(durations_ms)
        logger.info("")
        logger.info("Iteration Duration Statistics (from iteration boundaries):")
        logger.info(f"  Average: {avg_dur:.2f} ms")
        logger.info(f"  Min: {min_dur:.2f} ms")
        logger.info(f"  Max: {max_dur:.2f} ms")
        logger.info(f"  Range: {max_dur - min_dur:.2f} ms")

    # Calculate category breakdown
    category_totals: dict[str, float] = defaultdict(float)
    for s in stats:
        category_totals[s.category] += s.total_time_ms

    total_time = sum(category_totals.values())

    # Print results (matching GPU trace analyzer format exactly)
    logger.info("")
    logger.info("=" * 80)
    logger.info("TPU XLA Operations Analysis")
    logger.info(f"Total Iterations: {len(iterations) if iterations else 1} (real)")
    logger.info("Found 1 distinct iteration types")  # Simplified for TPU
    logger.info("=" * 80)
    logger.info("")
    logger.info("Iteration Type 1:")
    logger.info(f"  Count: {len(iterations) if iterations else 1} iterations (100.0%)")
    logger.info(f"  Total GPU Time: {total_time:.2f} ms/iteration")

    # Category breakdown line (same format as GPU analyzer)
    cat_parts = []
    for cat, time_ms in sorted(category_totals.items(), key=lambda x: -x[1]):
        pct = (time_ms / total_time * 100) if total_time > 0 else 0
        cat_parts.append(f"{cat}: {time_ms:.2f}ms ({pct:.1f}%)")
    logger.info(f"  Category Breakdown: {', '.join(cat_parts)}")

    # Top operations table
    logger.info(f"  Top {min(top_n, len(stats))} Kernels:")
    logger.info("  " + "-" * 88)
    header = f"{'Rank':<6}{'Category':<16}{'Kernel Name':<32}{'Time/Iter':<12}{'Time/Dev':<12}{'Calls/Iter':<12}{'%Total':<8}"
    logger.info("  " + header)
    logger.info("  " + "-" * 88)

    for i, s in enumerate(stats[:top_n], 1):
        op_name = s.name[:31] if len(s.name) > 31 else s.name
        row = (
            f"  {i:<6}{s.category:<16}{op_name:<32}"
            f"{s.total_time_ms:<11.2f} "
            f"{s.time_per_device_ms:<11.2f} "
            f"{s.num_calls:<11.1f} "
            f"{s.percentage_of_total:<7.1f}%"
        )
        logger.info(row)

    # Others row
    if len(stats) > top_n:
        others = stats[top_n:]
        others_time = sum(s.total_time_ms for s in others)
        others_time_per_dev = others_time / num_devices if num_devices > 0 else 0
        others_calls = sum(s.num_calls for s in others)
        others_pct = sum(s.percentage_of_total for s in others)
        row = (
            f"  {top_n + 1:<6}{'Others':<16}{'Others':<32}"
            f"{others_time:<11.2f} "
            f"{others_time_per_dev:<11.2f} "
            f"{others_calls:<11.1f} "
            f"{others_pct:<7.1f}%"
        )
        logger.info(row)

    logger.info("=" * 80)

    # CSV output (matching GPU trace analyzer format exactly)
    if csv_output:
        print("")
        print("=== CSV OUTPUT ===")
        print(
            "IterationType,Rank,Category,CategorySum_ms,KernelName,"
            "TimePerIter_ms,TimePerDev_ms,CallsPerIter,PercentTotal"
        )

        # Create a copy of category_totals for CSV (to add aggregated Others)
        csv_category_totals = dict(category_totals)

        # Prepare stats list for CSV - include aggregated Others if needed
        stats_for_csv = list(stats[:top_n])
        if len(stats) > top_n:
            others_stats = stats[top_n:]
            others_time = sum(s.total_time_ms for s in others_stats)
            others_calls = sum(s.num_calls for s in others_stats)
            others_pct = sum(s.percentage_of_total for s in others_stats)

            # Add aggregated Others to the "Others" category sum (matching GPU analyzer)
            if "Others" not in csv_category_totals:
                csv_category_totals["Others"] = 0.0
            csv_category_totals["Others"] += others_time

            others_op = TPUOpStats(
                name="Others",
                category="Others",
                total_time_ms=others_time,
                avg_time_per_call_ms=0.0,
                num_calls=others_calls,
                percentage_of_total=others_pct,
                time_per_device_ms=others_time / num_devices if num_devices > 0 else 0,
                original_name="Others",
            )
            stats_for_csv.append(others_op)

        # Sort by category, then by time descending (matching GPU analyzer)
        stats_for_csv.sort(key=lambda x: (x.category, -x.total_time_ms))

        # Track categories for sum display
        seen_categories: set[str] = set()
        rank = 1

        for s in stats_for_csv:
            # Show category sum only for first row of each category
            if s.category not in seen_categories:
                cat_sum = f"{csv_category_totals.get(s.category, 0.0):.4f}"
                seen_categories.add(s.category)
            else:
                cat_sum = ""

            # Use original name, escape commas
            op_name = s.original_name if s.original_name else s.name
            if "," in op_name:
                op_name = f'"{op_name}"'

            print(
                f"1,{rank},{s.category},{cat_sum},"
                f"{op_name},{s.total_time_ms:.4f},"
                f"{s.time_per_device_ms:.4f},"
                f"{s.num_calls:.2f},{s.percentage_of_total:.2f}"
            )
            rank += 1

        print("=== END CSV ===")
        print("")


def main() -> None:
    """Main entry point for TPU trace analyzer CLI."""
    parser = argparse.ArgumentParser(
        description="Analyze TPU profiler traces and display performance breakdown.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
Examples:
  python3 tpu_trace_analyzer.py trace.json.gz
  python3 tpu_trace_analyzer.py trace.json.gz --iteration-prefix "jit_step_fun" --top-n 20 --csv
        """,
    )

    parser.add_argument(
        "trace_path",
        type=str,
        help="Path to the TPU trace file (.json.gz or .json)",
    )
    parser.add_argument(
        "--iteration-prefix",
        "-p",
        type=str,
        default="jit_step_fun",
        help="Prefix for iteration boundary events (default: jit_step_fun)",
    )
    parser.add_argument(
        "--top-n",
        "-n",
        type=int,
        default=20,
        help="Number of top operations to display (default: 20)",
    )
    parser.add_argument(
        "--max-types",
        "-m",
        type=int,
        default=1,
        help="Maximum number of iteration types to display (default: 1)",
    )
    parser.add_argument(
        "--csv",
        action="store_true",
        help="Print kernel summary as CSV at the end of execution",
    )
    parser.add_argument(
        "--verbose",
        "-v",
        action="store_true",
        help="Enable verbose (debug) logging",
    )
    parser.add_argument(
        "--use-tf-op",
        action="store_true",
        default=True,
        help="Use tf_op field for semantic classification (default: enabled)",
    )
    parser.add_argument(
        "--no-tf-op",
        dest="use_tf_op",
        action="store_false",
        help="Disable tf_op-based classification, use kernel name only",
    )
    parser.add_argument(
        "--check-requests",
        action="store_true",
        help="Only check execute_model request/token counts, skip full analysis",
    )

    args = parser.parse_args()

    if args.verbose:
        logging.getLogger().setLevel(logging.DEBUG)

    try:
        if args.check_requests:
            check_execute_model_requests(args.trace_path)
            return

        analyze_tpu_trace(
            trace_path=args.trace_path,
            iteration_prefix=args.iteration_prefix,
            top_n=args.top_n,
            max_types=args.max_types,
            csv_output=args.csv,
            use_tf_op=args.use_tf_op,
        )
    except FileNotFoundError as e:
        logger.error(str(e))
        sys.exit(1)
    except Exception as e:
        logger.error(f"Error analyzing trace: {e}")
        raise


if __name__ == "__main__":
    main()
