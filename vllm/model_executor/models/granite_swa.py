"""Inference-only IBM GraniteSWA model compatible with HuggingFace weights.

GraniteSWA is Granite with per-layer sliding window attention and a learnable
per-head attention sink. Relative to `granite.py`, the only differences live in
the attention module: each layer reads its attention type from
`config.layer_types` (full vs. sliding window) and carries a learnable `sinks`
parameter that is passed to vLLM's `Attention` layer (a per-head bias in the
softmax denominator, equivalent to the Transformers `sigmoid(lse - sink)`).
"""

